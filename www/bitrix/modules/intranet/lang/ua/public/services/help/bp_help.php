   <p> Модуль <b> Документообіг </b> дозволяє призначати співробітників, відповідальних за переклад  документа в/із статусу в статус, і призначати співробітників, що мають права на редагування документа в тому чи іншому статусі. Також забезпечується зберігання копій документів в залежності від налаштувань цього модуля. Настройку може виробляти тільки адміністратор порталу. </P>


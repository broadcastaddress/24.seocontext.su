   <p> Спочатку в дистрибутиві корпоративного порталу передбачено всього три статусу, достатніх для найпростішої схеми документообігу. Для створення реальної схеми документообігу, що діє в конкретному проекті необхідно додавання нових статусів. Статуси може створювати тільки адміністратор порталу, або співробітники, наділені відповідними правами доступу до управління порталом. </P>

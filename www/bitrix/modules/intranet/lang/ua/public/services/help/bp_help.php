   <h1><a name='bizproc'></a> Бізнес-процеси </h1>

   <p> Модуль <b> Бізнес-процеси </b> - інструмент завдання, виконання та управління порядком звернення інформації. Це набагато більш функціональний інструмент, ніж модуль <b> Документообіг </b>. </p>

   <p><i><b> Бізнес-процес </b> - це процес обігу інформації (документів) за заздалегідь заданою схемою. У схемі бізнес-процесу можуть бути задані: </i></p>

   <ul>
     <li><i> одна або кілька точок входу і виходу; </i></li>
    <i></i>
     <li><i> послідовність дій (кроків, етапів, функцій), що здійснюються в заданому порядку і в певних умовах. </i></li>
    </ul>
  <i></i>
   <p></p>

   <p> Процеси обігу інформації в реальному предметної діяльності можуть бути самими різними. Публікація документа може зажадати самих різних порядків дій, завдання різних умов виконання, повідомлень співробітників і так далі. </P>

   <p> Модуль <b> Бізнес-процесів </b> має універсальний  механізм створення і редагування бізнес-процесів, доступний співробітнику - не програмісту. Але треба розуміти, що створення схеми бізнес-процесу вимагає певного аналітичного складу розуму і детального знання реального процесу, що діє в компанії. </P>

   <p> Такий універсальний механізм реалізований за рахунок візуального програмування за технологією <b> drag & amp; drop </b> (бери і тягни), зрозумілою і відомою будь-якому користувачеві комп'ютера. Шаблон бізнес процесів створюється в особливому візуальному конструкторі. Модуль <b> Бізнес-процесів </b> дозволяють співробітнику задати потрібний порядок проходження документа, відобразити специфічні потреби цього порядку за допомогою простих графічних схем. </P>

   <p> Порядок проходження інформації в компанії задається за допомогою шаблону бізнес-процесу, що конструюється з ряду дій. Дією може бути все, що завгодно: від створення документа до відправки електронної пошти або поновлення рядка в базі даних і так далі. </p>

   <p> У дистрибутив закладено низку вбудованих дій і типових бізнес-процесів, які можуть бути використані для виконання робіт загального призначення. Таких дій - кілька десятків, типових процесів - кілька примірників. </p>

   <p> За допомогою модуля <b> Бізнес-процеси </b> можуть бути описані два способи звернення документів: </p>

   <ul>
     <li><b> послідовний бізнес-процес </b> - дії виконуються одне за іншим від точки входу до точки виходу; </li>

     <li><b> бізнес-процес зі статусами </b> - початку і кінця не має, в процесі роботи відбувається перехід з одного стану в інший, може бути завершений в будь-якій точці. </li>
    </ul>


  <h1><a name='introduction'></a>Workflow und Geschäftsprozesse</h1>

  <p>Das <b>Bitrix Intranet</b> verfügt in seinen verschiedenen Editionen über zwei Module, welche eine gemeinsame Arbeit mit Dokumenten aller

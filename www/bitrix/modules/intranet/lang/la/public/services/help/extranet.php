<h3>Acceso y seguridad</h3>

<p>Los usuarios invitados que no son empleados de la compañía obtendrán un permiso de acceso limitado y, lo que es esencial, accesos sólo a las páginas Extranet. A estos usuarios no se les permite ver información privada de la empresa. Al mismo tiempo, los empleados de la compañía pueden ser invitados y unirse a los grupos de trabajo de Extranet para trabajar junto con colegas y socios: discutir problemas esenciales en foros y blogs; trabajar con documentos compartidos; planear actividades en los calendarios; asignar tareas y ver su progreso; publicar informes y muchos más.<br />
</p>

<h3>Equipo de trabajo</h3>

<p>Entonces ¿Cuáles son las ventajas de la Extranet al ser un área neutral para reuniones y colaboración? Fundamentalmente y tecnológicamente, la solución utiliza los mismos principios en los que basa el Portal de Intranet del tal manera que el trabajo en equipo desde la Extranet es absolutamente análogo al de los grupos de trabajo en la Intranet. La única diferencia radica en políticas de seguridad más estrictas.<br />
